package go4lunch.data.model;

public class Restaurant {

    private String name;
    private double lat;
    private double lon;

    public Restaurant(String name, double lat, double lon) {
        this.name = name;
        this.lat = lat;
        this.lon = lon;
    }

    public String getName() {
        return name;
    }

    public double getLat() {
        return lat;
    }

    public double getLon() {
        return lon;
    }
}
